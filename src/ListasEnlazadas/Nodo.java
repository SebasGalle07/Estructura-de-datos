package ListasEnlazadas;

public class Nodo {
   Elemento dato;
   Nodo enlace;

    public Nodo(Elemento dato, Nodo enlace) {
        this.dato = dato;
        this.enlace = enlace;
    }

    public int getDato() {
        return getDato();
    }

    public Nodo getEnlace() {
        return enlace;
    }

}
